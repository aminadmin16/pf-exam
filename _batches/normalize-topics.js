let total = 0;
MAP.forEach(([oldT, newT]) => {
  const from = '"topic": "' + oldT + '"';
  const to = '"topic": "' + newT + '"';
  const n = s.split(from).length - 1;
  if (n) { s = s.split(from).join(to); total += n; console.log("  " + oldT + " → " + newT + "  (" + n + ")"); }
});
fs.writeFileSync(file, s, "utf8");
console.log("รวมแก้ topic ทั้งหมด:", total, "ตำแหน่ง");

// พิมพ์จำนวนข้อต่อหมวดของวิชา analytical หลังรวม
const win = {};
new Function("window", s)(win);
const counts = {};
win.QUESTIONS.filter((q) => q.subject === "analytical").forEach((q) => { counts[q.topic] = (counts[q.topic] || 0) + 1; });
const rows = Object.keys(counts).map((t) => [t, counts[t]]).sort((a, b) => b[1] - a[1]);
console.log("\n== analytical topics (" + rows.length + " หมวด) ==");
rows.forEach(([t, c]) => console.log((c < 10 ? "  ⚠ " : "    ") + c + "\t" + t));
console.log("\nหมวดที่ยังน้อยกว่า 10:", rows.filter((r) => r[1] < 10).map((r) => r[0] + "(" + r[1] + ")").join(", ") || "ไม่มี");
